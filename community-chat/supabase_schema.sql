-- 3. MESSAGES TABLE
create table if not exists public.messages (
  id bigint generated by default as identity primary key,
  room_id text references public.rooms(id) not null,
  user_id uuid references public.profiles(id) not null,
  content text not null,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

alter table public.messages enable row level security;

do $$ begin
  drop policy if exists "Messages are viewable by everyone." on public.messages;
  drop policy if exists "Authenticated users can insert messages." on public.messages;
end $$;

create policy "Messages are viewable by everyone." on messages for select using ( true );
create policy "Authenticated users can insert messages." on messages for insert with check ( auth.role() = 'authenticated' or auth.role() = 'anon' );

-- 4. REALTIME SETUP
-- Add table to publication if not already added trying to match carefully
do $$ begin
  if not exists (select 1 from pg_publication_tables where pubname = 'supabase_realtime' and tablename = 'messages') then
    alter publication supabase_realtime add table messages;
  end if;
end $$;
